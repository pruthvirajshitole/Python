'''
Arithmetic Operators  (+,-,*,/,%,//,**)

'''

# Addition (+)
a = 10
b = 20
c = a+b
print(f"{a} + {b} = {c}")  # 10 + 20 = 30

# Subtraction (-)
d = 20
e = 10 
f = d-e
print(f"{d} - {e} = {f}")  # 20 - 10

# Multiplication (*)
g = 5
h = 6
i = g*h
print(f"{a} * {b} = {i}")  # 5 * 6 = 30

# Division (/)
j = 10
k = 2
l = j/k
print(f"{j} / {k} = {l}")  # 10 / 2

# Modulus (%)   (Remainder)
m = 10
n = 3
o = m%n
print(f"{m} % {n} = {o}")  # 10 % 3

# Floor Division (//)  (Quotient)
p = 10
q = 3
r = p//q
print(f"{p} // {q} = {r}")  # 10 // 3

# Exponention (**)
s = 2
t = 3
u = s**t
print(f"{s} ** {t} = {u}")  # 2 ** 3