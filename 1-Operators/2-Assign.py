'''
Assignments Operators (=,+=,-=,*=,/=,%=,//=,**=)

'''

a = 3  # Assigns the value 3 to the variable a
a += 5  # a = a+5 , so a = 3+5 = 8
print(f"{a}+=5 : {a}")  

a -= 2  # a = a-2 , so a = 8-2 = 6
print(f"{a}-=2 : {a}")  

a *= 3  # a = a*3 , so a = 6*3 = 18
print(f"{a}*=3 : {a}")  

a /= 2  # a = a/2 , so a = 18/2 = 9
print(f"{a}/=2 : {a}")

a %= 5  # a = a%5 , so a = 9%5 = 4
print(f"{a}%=5 : {a}")

a //= 2  # a = a//2 , so a = 4//2 = 2
print(f"{a}//=2 : {a}")

a **= 2 # a = a**2 , so a = 2**2 = 4
print(f"{a}**=2 : {a}")